"""Κανονικοποίηση βάσης δημοσκοπήσεων (πρόθεση ψήφου → εκτίμηση).

Πρόβλημα: άλλες μετρήσεις δημοσιεύονται ως «πρόθεση ψήφου» (με αναποφάσιστους
στη βάση) και άλλες ως «εκτίμηση» (χωρίς). Ο απευθείας μέσος όρος τους είναι
μεθοδολογικά λάθος (συστηματική απόκλιση 2-3 μονάδων).

Λύση (standard, ίδια με τη «μέθοδο των τριών» που εφαρμόζει και η Wikipedia):
αν μια μέτρηση δηλώνει ποσοστό αναποφάσιστων U, τα ποσοστά των κομμάτων
ανάγονται στη βάση των αποφασισμένων:  p' = p * 100 / (100 - U).

Εφαρμόζεται ΜΟΝΟ όταν υπάρχει δηλωμένο U > 0 — οι «εκτιμήσεις» μένουν ως έχουν.
Κάθε κανονικοποιημένη μέτρηση σημαδεύεται (normalized=True) για διαφάνεια
στον πίνακα ποιοτικής αξιολόγησης.
"""


def normalize_poll(poll):
    u = poll.get("undecided")
    if not u or u <= 0 or u >= 60:
        poll["normalized"] = False
        return poll
    factor = 100.0 / (100.0 - u)
    poll["vals"] = [round(v * factor, 1) for v in poll["vals"]]
    poll["normalized"] = True
    return poll


def normalize_all(polls):
    return [normalize_poll(p) for p in polls]
